public class Libro {

    private String titolo;
    private String autore;
    private String smb;

    public Libro(String titolo, String autore, String smb) {
        this.titolo = titolo;
        this.autore = autore;
        this.smb = smb;
    }

    public String getTitolo() {
        return titolo;
    }
}
